let colors = ['red', 'blue'];

console.log(colors);

// Accessing elements by index in square bracket.
// JS is dynamic.
// Which means that we can add a value at index 2.

colors[2] = 'orange';

// Not necessary that the data type should be same.
// We can add a number in this string array.
colors[3] = 20;

console.log(colors);


