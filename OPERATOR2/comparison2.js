let x = 1;

// Returns a boolean value.

// Relational
console.log(x>0);
console.log(x>=1);
console.log(x<1);
console.log(x<=1);

// Equality
console.log(x === 1);
console.log(x !== 1) ;



// EQUALITY OPERATOR.

// Strict Equality Operator. 
// Same type and value is necessary for true.
// Here we will get false because the type is not same.
console.log(1==='1');


// Lose Equality.
// Here only value should be same.
// It converts the type and then checks for value.
// If types are different then it does'nt matter as long as value is same.
console.log(1 == 1);
console.log(1==true);


