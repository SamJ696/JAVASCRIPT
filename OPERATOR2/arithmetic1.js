let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);


// x to the power y. Here 10 power 3 => 1000.
console.log(x**y);


// Increment
// First use then increment.
console.log(x++);

// Increment then use.
console.log(++x);


// Decrement
// Same as increment.
console.log(y--);
console.log(--y);
